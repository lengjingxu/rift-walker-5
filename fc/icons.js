// 工具：把 2D 数组转为 SVG rect
function makePixelIcon(grid, palette) {
  // grid: 二维数组，值为 palette key 或 undefined
  let rects = '';
  for (let y = 0; y < grid.length; y++) {
    for (let x = 0; x < grid[y].length; x++) {
      const c = grid[y][x];
      if (c && palette[c]) {
        rects += `<rect x="${x}" y="${y}" width="1" height="1" fill="${palette[c]}"/>`;
      }
    }
  }
  return `<svg viewBox="0 0 16 16" xmlns="http://www.w3.org/2000/svg" shape-rendering="crispEdges">${rects}</svg>`;
}

